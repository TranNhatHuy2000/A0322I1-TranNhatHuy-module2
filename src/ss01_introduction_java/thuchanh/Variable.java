package ss01_introduction_java.thuchanh;

public class Variable {
    public static void main(String[] args) {
        byte i = 10;
        float f = 20.5f;
        double d = 20.5d;
        boolean b = true;
        char c = 'a';
        String s = "Hà Nội";
        System.out.println(
                i + "\n"+
                f + "\n"+
                d + "\n"+
                b + "\n"+
                c + "\n"+
                s + "\n"
        );
    }
}
